package patterns.structural.examples.adapter;

public class TextView
{
	private float x = 0.0f;
	private float y;
	private float width;
	private float height;
	
	public TextView(float x, float y, float width, float height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
	
}
